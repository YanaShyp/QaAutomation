# task 1: Створіть дві змінні first=10, second=30. Виведіть на екран результат математичної
# взаємодії (+, -, *, / и тд.) для цих чисел.

first = 10
second = 30

result = first + second
print(result)

result = first - second
print(result)

result = first * second
print(result)

result = first / second
print(result)

result = first // second
print(result)

first += second
print(first)

result = first ** second
print(result)

# task 2: Створіть змінну и по черзі запишіть в неї результат порівняння (<, > , ==, !=) чисел з завдання 1.
# Виведіть на екран результат кожного порівняння.

third = first > second
print(third)

third = first < second
print(third)

third = first == second
print(third)

third = first != second
print(third)

